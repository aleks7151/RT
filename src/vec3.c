#include "rt.h"

float	dot(t_vec3 v1, t_vec3 v2)
{
	float d;

	d = v1.x * v2.x + v1.y * v2.y + v1.z * v2.z;
	return (d);
}

float	modul(t_vec3 v)
{
	float d;

	d = sqrt(v.x * v.x + v.y * v.y + v.z * v.z);
	return (d);
}
